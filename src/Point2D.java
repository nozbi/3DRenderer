public class Point2D 
{
    private int x;
    private int y;

    public Point2D(int xParameter, int yParameter)
    {
        this.x = xParameter;
        this.y = yParameter;
    }

    public int getX()
    {
        return this.x;
    }

    public int getY()
    {
        return this.y;
    }
}
